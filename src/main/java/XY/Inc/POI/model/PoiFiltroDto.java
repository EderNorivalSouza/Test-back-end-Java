package XY.Inc.POI.model;

public class PoiFiltroDto {
	

	private Integer coordX;
	private Integer coordY;
	
	
	public PoiFiltroDto(Integer coordX, Integer coordY) {
		this.coordX = coordX;
		this.coordY = coordY;
	}


	public Integer getCoordX() {
		return coordX;
	}


	public Integer getCoordY() {
		return coordY;
	}
	
	
}
	
